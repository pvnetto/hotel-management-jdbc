package model;

public class Rating {
	
	private int idRating;
	private int stars;
	
	public int getIdRating() {
		return idRating;
	}
	
	public void setIdRating(int idRating) {
		this.idRating = idRating;
	}
	
	public int getStars() {
		return stars;
	}
	
	public void setStars(int stars) {
		this.stars = stars;
	}
	
}
